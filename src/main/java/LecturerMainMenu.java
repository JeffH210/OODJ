class StudentManager {
    public void displayStudents(JTable table) {
        updateTableData(table, "student.txt"); 
    }

    public void searchStudent(String searchTerm, JTable table) {
        searchTableData(table, searchTerm, "student.txt"); 
    }

    public void displaySupervisees(JTable table) {
        updateTableData(table, "supervisees.txt");
    }

    public void searchSupervisee(String searchTerm, JTable table) {
        searchTableData(table, searchTerm, "supervisees.txt");  
    }

    private void updateTableData(JTable table, String filename) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0); // Clear the table

        try (BufferedReader br = new BufferedReader(new FileReader(filename))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                model.addRow(data);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void searchTableData(JTable table, String searchTerm, String filename) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0); // Clear the table

        try (BufferedReader br = new BufferedReader(new FileReader(filename))) {
            String line;
            while ((line = br.readLine()) != null) {
                if (line.contains(searchTerm)) {
                    String[] data = line.split(",");
                    model.addRow(data);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
}

