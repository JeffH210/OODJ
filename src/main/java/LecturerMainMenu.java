    class LecturerManager {
    public void searchLecturer(String staffID) {
        try (BufferedReader br = new BufferedReader(new FileReader("lecturer.txt"))) {
            String line;
            boolean found = false;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                if (data[0].equals(staffID)) {
                    found = true;
                    String lecturerDetails = "Staff ID: " + data[0] + "\n"
                            + "Name: " + data[1] + "\n"
                            + "Email: " + data[2] + "\n"
                            + "Contact: " + data[3];
                    JOptionPane.showMessageDialog(null, lecturerDetails, "Lecturer Details", JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
            }
            if (!found) {
                JOptionPane.showMessageDialog(null, "Lecturer with Staff ID " + staffID + " not found.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "An error occurred while reading the lecturer file.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
}

