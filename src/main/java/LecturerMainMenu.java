    public void confirmPresentation(JTable table) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        try (BufferedWriter bw = new BufferedWriter(new FileWriter("confirmedPresentations.txt", true))) { 
            for (int i = 0; i < model.getRowCount(); i++) {
                StringBuilder sb = new StringBuilder();
                for (int j = 0; j < model.getColumnCount(); j++) {
                    sb.append(model.getValueAt(i, j)).append(",");
                }
                bw.write(sb.toString());
                bw.newLine();
            }
            JOptionPane.showMessageDialog(null, "Presentations confirmed and saved.");
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

